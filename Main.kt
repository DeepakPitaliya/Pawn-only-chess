fun createBoard() {
    println("Pawns-Only Chess")
    val hLine = "  +---+---+---+---+---+---+---+---+"
    val vLine = "|   |   |   |   |   |   |   |   |"
    for (i in 0..16) {
        if (i == 3) {
            println("7 | B | B | B | B | B | B | B | B |")
            continue
        }
        if (i == 13) {println("2 | W | W | W | W | W | W | W | W |")
            continue
        }
        if (i % 2 == 0) println(hLine) else println("${9 - (i + 1)/2} " + vLine)
    }
    println("    a   b   c   d   e   f   g   h")
}
